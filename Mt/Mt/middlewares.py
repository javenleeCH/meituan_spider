class RotateUserAgentMiddleware(UserAgentMiddleware):
    def __init__(self,crawler ,user_agent=''):
        self.user_agent = user_agent
        super(RotateUserAgentMiddleware, self).__init__()

        self.ua = UserAgent()
        # 若settings中没有设置RANDOM_UA_TYPE的值默认值为random，
        # 从settings中获取RANDOM_UA_TYPE变量，值可以是 random ie chrome firefox safari opera msie
        self.ua_type = crawler.settings.get('RANDOM_UA_TYPE', 'random')

    @classmethod
    def from_crawler(cls, crawler):
        return cls(crawler)

    def process_request(self, request, spider):
        def get_ua():
            '''根据settings的RANDOM_UA_TYPE变量设置每次请求的User-Agent'''
            return getattr(self.ua, self.ua_type)
        ua = get_ua()
        request.headers.setdefault('User-Agent', get_ua())
        # 这句话用于随机选择user-agent
        ua = random.choice(self.user_agent_list)
        request.headers.setdefault('User-Agent', ua)

